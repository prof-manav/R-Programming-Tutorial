
# Assignment Operation in R
# There are three type of assignment operation in R
# 1: var = value (Equal Operator)
# 2: Var <- value (Leftward Operator)
# 3: value -> var (Rightward operator)
 

x = "Hello World!"
x                   # Print "Hello World!"

y <- "Hello World!"
y                   # Print "Hello World!"

"Hello World!" -> z
z                   # Print "Hello World!"
